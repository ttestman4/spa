import { createSelector, createFeatureSelector } from '@ngrx/store';
import * as fromRefState from './ref-state.reducer';
import * as fromCountry from './country/country.reducer';

/**
 * A selector function is a map function factory. We pass it parameters and it
 * returns a function that maps from the larger state tree into a smaller
 * piece of state. This selector simply selects the `books` state.
 *
 * Selectors are used with the `select` operator.
 *
 * ```ts
 * class MyComponent {
 *   constructor(state$: Observable<State>) {
 *     this.booksState$ = state$.pipe(select(getBooksState));
 *   }
 * }
 * ```
 */

/**
 * The createFeatureSelector function selects a piece of state from the root of the state object.
 * This is used for selecting feature states that are loaded eagerly or lazily.
 */
const getReferenceState = createFeatureSelector<fromRefState.ReferenceState>('refState');

/**
 * Every reducer module exports selector functions, however child reducers
 * have no knowledge of the overall state tree. To make them usable, we
 * need to make new selectors that wrap them.
 *
 * The createSelector function creates very efficient selectors that are memoized and
 * only recompute when arguments change. The created selectors can also be composed
 * together to select different pieces of state.
 */
const getCountryEntitiesState = createSelector(
  getReferenceState,
  state => state.country
);

/**
 * Adapters created with @ngrx/entity generate
 * commonly used selector functions including
 * getting all ids in the record set, a dictionary
 * of the records by id, an array of records and
 * the total number of records. This reduces boilerplate
 * in selecting records from the entity state.
 */
export const {
  selectIds: getCountryIds,
  selectEntities: getCountryEntities,
  selectAll: getAllCountries,
  selectTotal: getTotalCountries,
} = fromCountry.adapter.getSelectors(getCountryEntitiesState);
